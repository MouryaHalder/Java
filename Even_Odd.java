//WAJP to show weather a number is even or odd.(Statically or Compile time)
public class Even_Odd
 {
    public static void main(String[] args) {
        int n;
        n=7;
        if(n%2==0)
        {
            System.out.println("The number " +n+ " is Even Number");
        }
        else
        {
            System.out.println("The Number " +n+ " Is Odd  number"); //+n+  is Concatinating
        }
    }
}
